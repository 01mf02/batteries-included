module Pervasives = struct
  include Extlib.ExtPervasives.Pervasives

  let unique_mutex      = Mutex.create()
  let unique_ref        = ref 0
  let unique ()         =
    Mutex.lock unique_mutex;
    let result = Ref.post_incr unique_ref in
      Mutex.unlock unique_mutex;
      result
end
